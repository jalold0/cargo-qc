export function useT() {
  const { language } = useLanguageStore();
  return (key) => translations[language]?.[key] || translations.uz[key] || key;
}

export function useValueLabel() {
  const t = useT();
  return (value) => (VALUE_KEYS[value] ? t(VALUE_KEYS[value]) : value);
}
